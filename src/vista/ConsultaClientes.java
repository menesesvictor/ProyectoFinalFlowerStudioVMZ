    private void btnRegistrarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarPedidoActionPerformed
        conectarBase();
      
       objPedido.setNombre(txtNombre.getText());
       objPedido.setTelefono(Integer.parseInt(txtTelefono.getText()));
       objPedido.setDireccion(txtDireccion.getText());
       objPedido.setPedido(txtPedido.getText());
       objPedido.setCantidad(Integer.parseInt(txtCantidad.getText()));
       
       
       
       String altaPedido="INSERT INTO PEDIDO (`nombre`,`telefono`,`direccion`,`pedido`,`cantidad`) VALUES ('"+objPedido.getNombre()+"','"+objPedido.getTelefono()+"','"+objPedido.getDireccion()+"','"+objPedido.getPedido()+"','"+objPedido.getCantidad()+"')";
       
     
        try {
            stmt.executeUpdate(altaPedido);
            JOptionPane.showMessageDialog(null, "Pedido exitoso de:\n "+objPedido.getPedido());
            this.txtNombre.setText("");
            this.txtTelefono.setText("");
            this.txtDireccion.setText(null);
            this.txtPedido.setText(null);
            this.txtCantidad.setText(null);
        
            this.txtNombre.requestFocus(); 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error 3 de BD para Alta :\n\n"+ e);
        }
    }//GEN-LAST:event_btnRegistrarPedidoActionPerformed

